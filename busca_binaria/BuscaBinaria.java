    public static int buscaBinaria(int[] array, int inicio, int fim, int objetivo) {
        if (inicio > fim) {
            return -1;
        }

        int meio = (inicio + fim) >>> 1;

        if (array[meio] == objetivo) {
            return meio;
        }

        if (array[meio] > objetivo) {
            return buscaBinaria(array, inicio, meio - 1, objetivo);
        } else {

            return buscaBinaria(array, meio + 1, fim, objetivo);
        }
    }
